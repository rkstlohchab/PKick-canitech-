# Volume of cuboid
length = 4
breath = 5
height = 6

vol_cuboid = length * breath * height

print("The volume of cuboid is {}".format(vol_cuboid))

# Area of cuboid

area_cuboid = 2 * ((length * breath) + (breath * height) + (length * height))

print(f"And the area of cuboid is {area_cuboid}")